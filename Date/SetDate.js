// JavaScript Set Dates Methods
// set Date Methods let you set date values for a date Object

const x = new Date();
x.setDate(5);
x.setFullYear(2022);
x.setMonth(0);
x.setHours(5);
x.setMinutes(55);
x.setSeconds(12);

console.log(x);

const date = new Date("2002-01-22,5:30:22:100");
console.log(date);
