//Array push() method
//push() method adds elements to array

let cars = ["Thar","Mercedes","BMW","Bugatti"];
console.log(cars);
cars.push("lambo");
console.log(cars);

// we can also add element using length property

let car = ["Thar","Mercedes","BMW","Bugatti"];
car[car.length]='lambo';
console.log(car);
